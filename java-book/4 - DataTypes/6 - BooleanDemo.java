public class BooleanDemo {

    public static void main(String[] args) {
        int age = 18;

        boolean result;

        result = age >= 18;

        System.out.println(result);

        if (result)
        {
            System.out.println("You are eligible for voting");
        }
        else
        {
            System.out.println("You are not eligible for voting");
        }
    }
}
